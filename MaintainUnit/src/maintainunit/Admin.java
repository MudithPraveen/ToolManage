    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(userName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Enter The Admin Id to be Deleted");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/maintainUnitDB","User1","1234");
                String Id = userName.getText();
                PreparedStatement Add = Con.prepareStatement("DELETE FROM User1.ADMIN where USERNAME=?");
                Add.setString(1,Id);
                Add.executeUpdate();
                SelectAdmin();
                Clear();
                JOptionPane.showMessageDialog(this,"Admin Deleted Successfully");
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

