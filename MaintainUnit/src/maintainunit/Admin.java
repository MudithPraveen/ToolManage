    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        if(userName.getText().isEmpty()||FullName.getText().isEmpty()||Nic.getText().isEmpty()||Email.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information!");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/maintainUnitDB","User1","1234");
                PreparedStatement Ad = Con.prepareStatement("update User1.ADMIN set NIC=?,NAME=?,EMAIL=?,PASSWORD=? where USERNAME=?");
                Ad.setString(1,Nic.getText());
                Ad.setString(2,FullName.getText());
                Ad.setString(3,Email.getText());
                Ad.setString(4,password.getText());
                Ad.setString(5,userName.getText());
                Ad.executeUpdate();
                JOptionPane.showMessageDialog(this,"Admin Updated Successfully!");
                SelectAdmin();
                Clear();
            }catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

