    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        if(storeId.getText().isEmpty()||storeLocation.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information!");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/maintainUnitDB","User1","1234");
                PreparedStatement Ad = Con.prepareStatement("update User1.STORETD set LOCATION=? where STOREID=?");
                Ad.setString(1,storeLocation.getText());
                Ad.setString(2,storeId.getText());
                Ad.executeUpdate();
                JOptionPane.showMessageDialog(this,"Store Updated Successfully!");
                SelectStore();
            }catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

