    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(storeId.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Enter The Store Id to be Deleted");
        }
        else{
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/maintainUnitDB","User1","1234");
                String Id = storeId.getText();
                PreparedStatement Add = Con.prepareStatement("DELETE FROM User1.STORETD where STOREID=?");
                Add.setString(1,Id);
                Add.executeUpdate();
                SelectStore();
                JOptionPane.showMessageDialog(this,"Store Deleted Successfully");
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

